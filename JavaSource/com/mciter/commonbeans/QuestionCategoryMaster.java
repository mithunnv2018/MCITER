package com.mciter.commonbeans;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

@Entity
public class QuestionCategoryMaster {

	@Id
	@GeneratedValue
	private Integer QC_ID;
	private String QC_Name;
	private Integer QC_Tag;
	public Integer getQC_ID() {
		return QC_ID;
	}
	public void setQC_ID(Integer qC_ID) {
		QC_ID = qC_ID;
	}
	public String getQC_Name() {
		return QC_Name;
	}
	public void setQC_Name(String qC_Name) {
		QC_Name = qC_Name;
	}
	public Integer getQC_Tag() {
		return QC_Tag;
	}
	public void setQC_Tag(Integer qC_Tag) {
		QC_Tag = qC_Tag;
	}
	
}
